n = int(input("Type a number: "))

r1 = n + 5
r2 = n - 5
r3 = n * 5
r4 = n / 5
r5 = n ** 5
r6 = n // 5
r7 = n % 5

print("{} + 5 = {}".format(n, r1))
print("{} - 5 = {}".format(n, r2))
print("{} * 5 = {}".format(n, r3))
print("{} / 5 = {}".format(n, r4))
print("{} ** 5 = {}".format(n, r5))
print("{} // 5 = {}".format(n, r6))
print("{} % 5 = {}".format(n, r7))

